            function openmenumobile () {
                var elemento = document.getElementById('menu');
                elemento.style.display = 'block';
                if (window.getComputedStyle(elemento).display === "block") {
                    document.getElementById('openmenu').style.display = 'none';
                    document.getElementById('closemenu').style.display = 'block';
                    disableScrolling();
                }
            }

            function closemenumobile () {
                var elemento = document.getElementById('menu');
                elemento.style.display = 'none';
                if (window.getComputedStyle(elemento).display === "none") {
                    document.getElementById('openmenu').style.display = 'block';
                    document.getElementById('closemenu').style.display = 'none';
                    window.onscroll=function(){};
                }

            }

            document.getElementById('openmenu').addEventListener("click", () => openmenumobile());
            document.getElementById('closemenu').addEventListener("click", () => closemenumobile());

